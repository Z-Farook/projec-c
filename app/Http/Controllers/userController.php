    {
        //
        $user = new User();
        $user->name = ($request->input('name'));
        $user->email = ($request->input('email'));
        $user->password= ($request->input('password'));
        $user->admin= ($request->input('admin'));
        $user->save();
        if ($user)
            return response()->json($user, 200);
        else
            return response()->json("error");
    }

